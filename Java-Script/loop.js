document.write("For Loop"+"<br>")
for(var i=1;i<=5;i++)
{
    document.write(i+"<br>");
}

document.write("While Loop"+"<br>");
var x = 1;
while(x<=5)
{
    document.write(x+"<br>");
    x++;
}

document.write("Do While Loop"+"<br>");
var a = 1;
do{
    document.write(a+"<br>");
    a++;
}while(a<=5);